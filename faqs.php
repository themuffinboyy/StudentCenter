            <h1>Frequently Asked Questions</h1>
            <strong>How do I apply additional funds to my RowanCard?</strong>
            <p>Students cannot add Dining Dollars to their accounts, however, students, faculty, and staff can add 
            money to their 'Boro Bucks account. To do so, you have the option of doing this on-line through Banner 
            Self-Service if using financial aid credit, through your smart phone via the eAccounts app (in order 
            to make a deposit from your smart phone, you must first do an initial deposit through the on-line system 
            and save your payment method), in person at the Bursar's Office, or by sending a check payment to the Bursar's Office
            at 201 Mullica Hill Rd., Glassboro, NJ 08028 (Please include the student's Banner ID number on the check).</p>

            <strong>Can I let someone borrow my card?</strong>
            <p>Rights and privileges associated with the card are non-transferable. Students, faculty, and staff should not 
            lend their cards to friends or give them any passwords associated with the card accounts. You will be held 
            accountable for all financial and business transactions conducted with their RowanCard.</p>

            <p>Students, faculty, and staff should always carry their RowanCard with them as proof of who they are and their 
            affiliation with the university.</p>

            <strong>What if I find someone else's card?</strong>
            <p>Found cards should be returned to the Student Center Information Desk, just outside of the MarketPlace, or call 
            856-256-GONE for further instructions. Attempting to use a lost/stolen card, for any purpose, can be considered theft.</p>

            <strong>What if I lose my card?</strong>
            <p>You can freeze your RowanCard accounts on-line through the <a href="https://eacct-rowancard-sp.blackboard.com/eaccounts/AnonymousHome.aspx"
            target="_blank">
            myrowancard</a> site, from your smart phone via the eAccounts app or by calling 856-256-GONE to prevent unauthorized use of your card.</p>

            <p>To replace a RowanCard, simply go to the Information Desk located in the Chamberlain Student Center during normal business 
            hours to have a replacement card printed. Students are charged a replacement fee of $25. Contact the Card Services Office
            (856-256-4531) to have your RowanCard reactivated once your replacement card is made.</p>

            <strong>Who is eligible?</strong>
            <p>The RowanCard is required for all Rowan students, faculty and staff as their identification card.</p>

            <strong>If I'm on a meal plan can I still use the RowanCard?</strong>
            <p>Absolutely! You can draw upon four accounts when using the RowanCard—the Meal Plan, Dining Dollars account, 'Boro Bucks 
            account, and the Meal SV account (block plan holders only). Please be aware that Dining Dollars can only be used for food 
            purchases on-campus and at select Rowan Boulevard food locations. 'Boro Bucks can be used for food as well as any other 
            purchase wherever the RowanCard is accepted. Meal SV transactions can be used at selected Rowan Blvd. merchants.</p>

            <strong>How do I open my 'Boro Bucks account?</strong>
            <p>Every person who is issued a RowanCard also has the option of using his or her RowanCard 'Boro Bucks. There is no need to 
            “open” this account, simply make a deposit and then use your card at any of the participating merchants. Not sure on how 
            to make a deposit? <a href="/account.php"></a>Click here</a>.</p>

            <strong>How do I close my account?</strong>
            <p>The funds in the 'Boro Bucks Account automatically roll over from one semester to the next and year to year. You may request
            to close your 'Boro Bucks account at the end of any academic year by completing the clous-out form and submitting it to the
            Bursar's Office. Upon withdrawal from school or termination of employment, the RowanCard 'Boro Bucks account will be closed and
            a refund will be issued for balances over $10.00. Graduating students with balances over $10.00 will receive a refund check.</p>
