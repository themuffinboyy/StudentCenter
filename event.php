        business days prior to the event or a $25 late fee will be assessed. It is the sole responsibility of the reserving party to
        complete and provide this information. Student Center staff will be able to assist you in determining the most effective set-up
        arrangement for your function. Please consult with them well in advance of your event so that these deadlines can be met and
        charges can be avoided.
      </li><br>
      <li>
        Student Center Administrative Staff will assign each reservation to the most appropriate space(s) available based on the event
        description on the request. The demands on the facility and the nature and size of the event will be considered in determining
        space assignments. Requests for specific rooms or space will be honored when possible. The staff reserves the right to re-assign
        space when necessary and to identify suitable alternative space for the original reservation.
      </li><br>
      <li>
