            <a href="#panel12a">Public Safety Requirements</a>
            <div id="panel12a" class="content">
              <p>It is the responsibility of Public Safety and University Personnel to evaluate each event for its unique
              circumstances and potential risk factors. These factors include, but are not limited to: student vs nonstudent
              attendance, number of expected attendees, event venue, ticket sales/cash protection, off campus
              advertisement, nature/type of event, prior problems or history of event, advance notice of problem, requests
              for personal protection for performers, timing of event, and other factors that may be associated with
              conducting an event.</p>
              <p>By reviewing all known factors about a given event, and combining that information with knowledge of the
              campus, Public Safety will determine security and staffing needs for the event, as well as determine any
              capacity limitations for each event venue (based on safety issues). The event will be assigned a category
              according to the guidelines below. All questions can be directed to the Assistant Director of the Student
              Center, who is available to assist all student organizations with respect to policy adherence and event
              logistics.</p> 
