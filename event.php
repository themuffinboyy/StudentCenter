    <h3> General Reservation Procedures </h3>
    <hr>
    <ol>
      <li>
        To reserve rooms/space in the Student Center go to the Student Center website (<a href="http://www.rowan.edu/studentcenter">
        www.rowan.edu/studentcenter</a>) and click on the "Reserve a Room" Quick Link or visit <a href="http://www.rowan.edu/25live">
        www.rowan.edu./25live</a>. At the bottom of the page, click on "25Live". All users must have a valid Rowan email address to
