      <li>
        Space assignments are received and confirmed on a first come, first serve basis within the priority categories (see Scheduling
        Priority). A confirmation will be emailed to the individual identified on the request. The email should be carefully reviewed
        for confirmation or denial of space or equipment, particularly with requests for multiple dates. Please note that your
        reservation is complete when you receive a confirmation/cancellation of your request by email.
      </li><br>
      <li>
        Requests made less than 5 business days in advance will be assessed an administrative service fee of $25. Due to limited space
        and resources, users are highly encourages to plan and anticipate room needs well in advance of event.
      </li><br>
      <li>
        The 25Live scheduling systems will not allow online reservations to be made less than 5 days in advance. All requests for space
        within the five-day window must be done in-person to the Student Center main administration office 117A during weekday business
        hours.
