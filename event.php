    <h3> Special Event and Late Night Programming Policy </h3>
    <hr>
    <p>
      Rowan University endeavors to provide for the safety of students and guests attending campus events. These guidelines apply to 
      late night and special events and are intended to complement the University's regular reservation policies and procedures regarding 
      campus space. For purposes of this policy, “late night events” shall mean events expected to end at 11pm or later; and “special events”
      shall mean events with anticipated attendance of over 200 students/guests or events that may pose a security concern for some other 
      reason, as determined by Public Safety and/or University Personnel. Student Organizations chartered by SGA are permitted to reserve 
      space in the Student Center and throughout the campus for extracurricular activities such as shows, dance parties, concerts, semi-formal 
      activities, performances or other special events. Space will be assigned based on availability, type of activity, number of participants, 
      and location of other events within the facility. Special event/late night venues include the following: Student Center Pit, Student Center 
      Ballroom, Student Center Patio, Rec Center, Esby Gym, Intramural Field, Varsity Fields, Academic Building Atriums (Science Hall, Education Hall, 
      Rowan Hall), Pfleeger Hall, Boyd Recital Hall, and Tohill Auditorium, as well as any parking lot area.
    <p>
    <p>
      Chartered student organizations must be in good standing with the University and SGA in order to sponsor an event on campus. Student organizations 
      that are on probation or that have outstanding financial obligations to the University or Dining Services may not hold events on campus. Chartered 
      clubs may not co-sponsor a special event or late night event with an off-campus organization or with any commercial enterprise. Appeals to this policy 
      must be in writing to the Assistant Vice President for Campus Recreation, Student Center and Conference Service, and may require a contract and insurance 
      from the external organization.
    <p>

    <h5><strong>Procedures for Sponsoring a Special Event or Late Night Program</strong></h5>
    <ol id="list_indent">
      <li id="indent">
        Completion of a Room Reservation Request by the organization must be made at least 20 business days (4 weeks) in advance. This reservation will remain 
        tentative until all requirements are met (see below). Reservations or requests for space should be made directly to the office or building in which the 
        event will be located. The Student Center staff, located in Room 117A, can assist in providing contact information for buildings throughout campus.
      </li><br>
      <li id="indent">
        Completion of Special Event/Late Night Registration Form at least 4 weeks in advance (form available in Student Center Main Office 117A and at 
        rowan.edu/studentcenter/about/forms). This form will be used to evaluate the event for potential risk as well as to determine public safety/staffing 
        needs. It is imperative that this form has detailed information about the event so that University personnel can review program logistics fairly and 
        accurately. It may be necessary to follow up with the event sponsor in person to further clarify any areas that appear unclear or non-specific.
      </li><br>
      <li id="indent">
        If it is determined by University personnel that a special events meeting is required (based on the final risk analysis), then it must be held at least 
        two weeks in advance of the program date. Representatives from the organization, along with their advisor, Public Safety representatives and appropriate 
        Student Center/Student Activities staff will be in attendance to thoroughly review event logistics. It is strongly recommended that a program proposal be 
        prepared for distribution at this meeting. Revisions and changes to the event may be requested at this meeting.
      </li><br>
      <li id="indent">
        Completion of necessary paperwork (SGA check requests forms, catering requests, equipment/AV requests, room diagrams) should be completed ten (10) business 
        days prior to event to ensure that all requests are met within a timely manner.
      </li><br>
    </ol>

