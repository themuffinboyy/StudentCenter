        contact the Student Center Office and someone can assist you with the process. For more information about 25Live, visit 
        <a href="http://www.rowan.edu/ces/25live/index.cfm">http://www.rowan.edu/ces/25live/index.cfm</a>. Please note that phone
        and email reservations will not be accepted.
